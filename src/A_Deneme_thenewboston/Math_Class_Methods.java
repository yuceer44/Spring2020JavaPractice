package A_Deneme_thenewboston;

public class Math_Class_Methods {

    public static void main(String[] args) {

        System.out.println(Math.abs(-23.4));//how much away from zero
        System.out.println(Math.ceil(12.3));  //up
        System.out.println(Math.floor(12.6));  //down
        System.out.println(Math.max(8.3,5.4));// between two numbers find the max value
        System.out.println(Math.min(8.3,5.4));
        System.out.println(Math.pow(5,3));//5*5*5=125
        System.out.println(Math.sqrt(9));//3*3





    }
}
